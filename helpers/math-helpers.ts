
export function addTwoNumber(num1:number,num2:number) {
    return num1 + num2;
}

export function subtractTwoNumber(num1:number,num2:number) {
    return num1 - num2;
}

export function timesTwoNumber(num1:number,num2:number) {
    return num1 * num2;
}

export function divideTwoNumber(num1:number,num2:number) {
    return num1 / num2;
}